/**
* Programa que cuenta desde el 0 
* hasta un numero introducido por teclado 
	* 
*
* @author Luis Ponce Garrido
*/
public class  Ejemplo4 {
	
	public static void main(String[] args) {

		int suma=0;
	
		for (int i = 100;  i <= 200; i=i+2){

		suma = suma + i;
		System.out.println(+suma);
		}
	
	
  
  }
}
